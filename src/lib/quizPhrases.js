// Funciones para obtener frases por nivel
export const getPhrasesByLevel = (level) => {
    if (level <= 2) {
        return [...PHRASES.saludos, ...PHRASES.cotidiano.slice(0, 15)];
    }
    if (level <= 4) {
        return [...PHRASES.saludos, ...PHRASES.cotidiano, ...PHRASES.restaurante, ...PHRASES.compras];
    }
    return Object.values(PHRASES).flat();
};

export const getAllPhrases = () => Object.values(PHRASES).flat();

export const getRandomPhrase = (level = 1) => {
    const phrases = getPhrasesByLevel(level);
    return phrases[Math.floor(Math.random() * phrases.length)];
};
